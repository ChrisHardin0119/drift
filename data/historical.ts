export const getTotalYearlyImpact = (): number => {
  return historicalChanges
    .filter(c => c.type === 'price_increase')
    .reduce((sum, c) => {
      const match = c.impact.match(/\$(\d+)/);
      return sum + (match ? parseInt(match[1]) : 0);
    }, 0);
};
